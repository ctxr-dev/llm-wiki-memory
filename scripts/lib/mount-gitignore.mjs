// Negated `.gitignore` body for a federated wiki MOUNT.
//
// A mount is a `.llm-wiki-memory` data dir that lives inside a CONSUMING
// project's git repo. Its shared (`ownership: repo`) wiki categories are meant
// to be committed by the USER as part of that repo; everything else (personal
// categories, the per-user layout override, embedding caches, generated index
// files, engine internals + runtime state) must stay OUT of the repo.
//
// The generated body is NEGATED: it ignores the whole mount, then re-includes
// exactly the shared category dirs — re-including every ancestor dir on the way
// (git will not descend into an ignored directory, so a nested `!` can never
// re-surface a leaf whose parent is still excluded). Generated caches and index
// files inside a tracked category are then re-ignored by trailing un-anchored
// patterns (later patterns win), so the shared *content* rides in git while its
// derived artefacts do not.

import { sharedCategories } from "./wiki-ownership.mjs";

const HEADER = [
  "# llm-wiki-memory mount .gitignore (generated by bootstrap; safe to re-run).",
  "# Tracks ONLY shared (ownership: repo) wiki categories. The engine never",
  "# commits this repo — you commit shared memory yourself. Personal categories,",
  "# the layout.local.yaml override, embedding caches, index.md, and engine",
  "# internals/state are ignored. Edit ownership in wiki/.layout/layout.yaml.",
];

/**
 * Build the `.gitignore` body for a mount, given the mount's (merged) layout.
 * The body is written at the mount data-dir root (`<mount>/.llm-wiki-memory/`),
 * so every anchored pattern is relative to that directory and the wiki tree is
 * addressed as `wiki/…`.
 * @param {Record<string, unknown>} layout the mount's merged layout object
 * @returns {string} the `.gitignore` file body (newline-terminated)
 */
export function buildMountGitignore(layout) {
  const shared = sharedCategories(layout || {});
  const lines = [
    ...HEADER,
    "",
    "# Ignore the whole mount by default; re-include only the shared contract.",
    "/*",
    "!/.gitignore",
    "",
    "# Descend into the wiki tree.",
    "!/wiki/",
    "/wiki/*",
    "",
    "# Shared layout contract (but drop the personal layout.local.yaml override).",
    "!/wiki/.layout/",
    "/wiki/.layout/*",
    "!/wiki/.layout/layout.yaml",
    "",
    "# Shared (repo-owned) categories — tracked:",
  ];
  for (const cat of shared) lines.push(`!/wiki/${cat}/`);
  lines.push(
    "",
    "# Never track generated caches or index files inside tracked categories.",
    ".embeddings/",
    "index.md",
    "",
  );
  return lines.join("\n");
}
