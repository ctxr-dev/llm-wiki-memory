#!/usr/bin/env node
import path from "node:path";
import fs from "node:fs";
import { pathToFileURL } from "node:url";

// Pure string builders for the scheduled cron-job (the launchd plist, the crontab
// wrapper, the cron line, the idempotency filter). The OS calls (cksum, launchctl,
// crontab) stay in bootstrap.sh; this owns only the byte-exact text so it is
// unit-testable. `ws_hash` is passed IN (bash computes the POSIX cksum) so install
// and uninstall derive the same label/tag/paths without a JS cksum reimplementation.

/** @param {string} s @returns {string} — `&` first so introduced entities aren't re-escaped */
export function xmlEscape(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

/**
 * @param {{ wsHash: string, workspaceDir: string, dataDir: string, home: string }} o
 * @returns {{ label: string, plistPath: string, tag: string, wrapperPath: string }}
 */
export function deriveScheduleIds({ wsHash, workspaceDir, dataDir, home }) {
  const label = `com.llm-wiki-memory.${wsHash}`;
  return {
    label,
    plistPath: path.join(home, "Library", "LaunchAgents", `${label}.plist`),
    tag: `# llm-wiki-memory:${workspaceDir}`,
    wrapperPath: path.join(dataDir, "state", "cron-daily.sh"),
  };
}

/**
 * @param {{ label: string, dataDir: string, nodeBin: string, cliPath: string, cronPath: string }} o
 * @returns {string}
 */
export function renderPlist({ label, dataDir, nodeBin, cliPath, cronPath }) {
  const e = xmlEscape;
  return `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
  <key>Label</key>
  <string>${e(label)}</string>
  <key>EnvironmentVariables</key>
  <dict>
    <key>MEMORY_DATA_DIR</key>
    <string>${e(dataDir)}</string>
    <key>PATH</key>
    <string>${e(cronPath)}</string>
  </dict>
  <key>ProgramArguments</key>
  <array>
    <string>${e(nodeBin)}</string>
    <string>${e(cliPath)}</string>
    <string>cron-job</string>
  </array>
  <key>StartCalendarInterval</key>
  <dict>
    <key>Minute</key>
    <integer>0</integer>
  </dict>
</dict>
</plist>
`;
}

/**
 * @param {{ dataDir: string, cronPath: string, nodeBin: string, cliPath: string }} o
 * @returns {string}
 */
export function renderWrapper({ dataDir, cronPath, nodeBin, cliPath }) {
  return `#!/usr/bin/env bash
# Auto-generated by llm-wiki-memory bootstrap.sh — invoked HOURLY by cron.
# The cron-job subcommand handles compile + consolidate + structured
# attempt logging. Do NOT hand-edit; re-run bootstrap.sh to regenerate.
set -u
export MEMORY_DATA_DIR="${dataDir}"
export PATH="${cronPath}"
exec "${nodeBin}" "${cliPath}" cron-job
`;
}

/** @param {{ wrapperPath: string, tag: string }} o @returns {string} */
export function renderCronLine({ wrapperPath, tag }) {
  return `0 * * * * "${wrapperPath}" ${tag}`;
}

/**
 * Keep every crontab line EXCEPT the one whose EXACT suffix is `tag` (a
 * prefix-sibling workspace is safe — never `grep -vF`); then drop ALL blank
 * lines (matches the shell `grep -v '^$'`). `addLine` is appended if given.
 * @param {string} text @param {string} tag @param {string} [addLine]
 * @returns {string} the new crontab text (trailing newline)
 */
export function filterCrontab(text, tag, addLine) {
  const kept = String(text)
    .split("\n")
    .filter((l) => l !== "" && !l.endsWith(tag));
  if (addLine) kept.push(addLine);
  return kept.length ? `${kept.join("\n")}\n` : "";
}

if (import.meta.url === pathToFileURL(process.argv[1] || "").href) {
  const [cmd, ...rest] = process.argv.slice(2);
  const out = (/** @type {string} */ s) => process.stdout.write(s);
  if (cmd === "plist") {
    const [label, dataDir, nodeBin, cliPath, cronPath] = rest;
    out(renderPlist({ label, dataDir, nodeBin, cliPath, cronPath }));
  } else if (cmd === "wrapper") {
    const [dataDir, cronPath, nodeBin, cliPath] = rest;
    out(renderWrapper({ dataDir, cronPath, nodeBin, cliPath }));
  } else if (cmd === "cron-line") {
    const [wrapperPath, tag] = rest;
    out(`${renderCronLine({ wrapperPath, tag })}\n`);
  } else if (cmd === "filter-crontab") {
    const [tag, addLine] = rest;
    out(filterCrontab(fs.readFileSync(0, "utf8"), tag, addLine));
  } else if (cmd === "ids") {
    const [wsHash, workspaceDir, dataDir, home] = rest;
    const ids = deriveScheduleIds({ wsHash, workspaceDir, dataDir, home });
    out(`${ids.label}\t${ids.plistPath}\t${ids.tag}\t${ids.wrapperPath}\n`);
  } else {
    console.error("usage: render-schedule.mjs <plist|wrapper|cron-line|filter-crontab|ids> ...");
    process.exit(1);
  }
}
